    <h2>About Koovi</h2>

 <p>Few months ago we had a revelation while on our monthly visit to the local market. Since we were late, a lot of the groceries were sold out and all the farmers have packed up their allotment. A few vendors stayed back to sell out the remaining last portions of their produce. Surprisingly, these last portions were incredibly cheaper than they were during the beginning of the sales.</p>

 <p class="para_shadow">“ Closing sale at lowest price!” , called out a farmer who was trying to sell out the last few bundles of his spinach.</p>
 
 <p>We bought the items and headed back with a feeling of contentment.</p>

 <p>So we thought, why not connect them to a bigger network? Why not support these small businesses to reach out to the world and create a brand for themselves? At the same time this will also give the same feeling of contentment we had to a bigger customer circle.

 </p>
   
 <p class="para_shadow2">And that was the idea that started KOOVI.</p>

 <p>Koovi is an outreach platform that helps vendors and customers connect within the touch of a notification. Customers can get lowest prices using the day to day deals while small vendors are able to reach out to a bigger audience and develop their brand.</p>

    
